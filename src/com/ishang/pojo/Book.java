package com.ishang.pojo;

public class Book {
	int Bid;
	String Bname;
	String Bauthor;
	String Bphouse;
	float Buprice;
	public int getBid() {
		return Bid;
	}
	public void setBid(int bid) {
		Bid = bid;
	}
	public String getBname() {
		return Bname;
	}
	public void setBname(String bname) {
		Bname = bname;
	}
	public String getBauthor() {
		return Bauthor;
	}
	public void setBauthor(String bauthor) {
		Bauthor = bauthor;
	}
	public String getBphouse() {
		return Bphouse;
	}
	public void setBphouse(String bphouse) {
		Bphouse = bphouse;
	}
	public float getBuprice() {
		return Buprice;
	}
	public void setBuprice(float buprice) {
		Buprice = buprice;
	}
}
